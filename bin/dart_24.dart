void main() {
  // bool 类型可以赋值 true 或 false
  bool isTrue = true;
  isTrue = false;
  print(isTrue);

  // == 判断是否相等，如果相等返回true
  var temp = 1 == 2;
  print(temp);

  // != 判断是否不相等，如果不相等返回true
  temp = 1 != 2;
  print(temp);
}
